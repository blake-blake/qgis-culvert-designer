class CulvertDesignerAlgorithm(QgsProcessingAlgorithm):
   
    OUTPUT = 'OUTPUT'
    INPUT = 'INPUT'

    wbt = WhiteboxTools()

    def name(self):
        """
        Returns the algorithm name, used for identifying the algorithm.
        """
        return 'culvert_designer'

    def displayName(self):
        return self.tr("Run full Culvert Designer workflow")
    
    def groupId(self):
        return ''
    
    def group(self):
        return self.tr(self.groupId())

    def tr(self, string):
        return QCoreApplication.translate('Processing', string)

    def createInstance(self):
        return CulvertDesignerAlgorithm()

    def icon(self):
        cmd_folder = os.path.split(inspect.getfile(inspect.currentframe()))[0]
        icon = QIcon(os.path.join(os.path.join(cmd_folder, 'icon.jpg')))
        return icon


    def update_progress(self, feedback):
        """
        Helper function to update the QGIS UI progress bar
        """
        feedback.setCurrentStep(self.step_counter)
        if feedback.isCanceled():
            return {}
        self.step_counter += 1


    # This shows the inputs in the QGIS dialogue box.
    def initAlgorithm(self, config):
        """
        Here we define the inputs and output of the algorithm, along
        with some other properties.
        """

        defaults = DesignParams()

        ## INPUTS
        # We add the input vector features source. It can have any kind of geometry.

        self.addParameter(QgsProcessingParameterEnum(
            PARAM_RAIN_METHOD,
            'What rainfall region would you like?',
            options= RAIN_METHODS,
            allowMultiple=False,
            usesStaticStrings=False,
            defaultValue=0
        ))

        self.addParameter(QgsProcessingParameterFile(
            PARAM_BASE_FOLDER,
            'Where would you like all intermediate and final output files saved?',
            behavior=QgsProcessingParameterFile.Folder,
            fileFilter='All files (*.*)',
            defaultValue=None
        ))

        self.addParameter(QgsProcessingParameterFeatureSource(
                PARAM_ROAD,
                self.tr('What is the file representing the road batter toes?'),
                [QgsProcessing.TypeVectorAnyGeometry]
        ))

        # This is the RASTER input
        self.addParameter(QgsProcessingParameterRasterLayer(
                PARAM_DEM,
                'What file is your 1-5m resolution elevation model?',
                defaultValue=None
        ))

        self.addParameter(QgsProcessingParameterNumber(
                PARAM_THRESHOLD_ORDER,
                self.tr('Select the threshold for defining major streams, a higher number will produce less culvert banks'),
                type=QgsProcessingParameterNumber.Integer,
                defaultValue=8,
                minValue=1
        ))

        self.addParameter(QgsProcessingParameterNumber(
                PARAM_ROAD_WIDTH,
                self.tr('What is the approximate road width, to group inlets with outlets'),
                type=QgsProcessingParameterNumber.Integer,
                defaultValue=50,
                minValue=1
        ))

        #Advanced parameters (defaults with optional changes)
        existing_strahler = QgsProcessingParameterFile(
                PARAM_EXISTING_LDD, 
                'Have you already undertaken analysis on your project elevation model? Select the lddcreate map file to speed up future iterations', 
                optional=True, 
                behavior=QgsProcessingParameterFile.File, 
                fileFilter='Flow Direction File (*.map)',
                defaultValue= None 
        ) 
        existing_strahler.setFlags(existing_strahler.flags() | QgsProcessingParameterDefinition.FlagAdvanced)
        self.addParameter(existing_strahler)

        param = QgsProcessingParameterBoolean(
            PARAM_ADD_TO_PROJECT, self.tr('Load outputs to the project?'), defaultValue=True)
        param.setFlags(param.flags() | QgsProcessingParameterDefinition.FlagAdvanced)
        self.addParameter(param)

        param = QgsProcessingParameterNumber(
            PARAM_HEADWATER_LIMIT, self.tr('Max allowable headwater ratio (Hw/D)'),
            QgsProcessingParameterNumber.Double, defaultValue=defaults.headwater_limit, minValue=0.1
        )
        param.setFlags(param.flags() | QgsProcessingParameterDefinition.FlagAdvanced)
        self.addParameter(param)
        
        param = QgsProcessingParameterNumber(
            PARAM_MANNINGS_N, self.tr("Manning's n for corrugated metal pipe"),
            QgsProcessingParameterNumber.Double, defaultValue=defaults.mannings_n, minValue=0.01, maxValue=0.2
        )
        param.setFlags(param.flags() | QgsProcessingParameterDefinition.FlagAdvanced)
        self.addParameter(param)
        
        param = QgsProcessingParameterNumber(
            PARAM_SNAP_DIST, self.tr('Pour point snap distance (cells/pixels)'),
            QgsProcessingParameterNumber.Double, defaultValue=defaults.snap_distance, minValue=0.0
        )
        param.setFlags(param.flags() | QgsProcessingParameterDefinition.FlagAdvanced)
        self.addParameter(param)
        
        param = QgsProcessingParameterNumber(
            PARAM_AREA_FACTOR, self.tr('Area sensitivity factor (multiplier)'),
            QgsProcessingParameterNumber.Double, defaultValue=defaults.area_factor, minValue=1.0
        )
        param.setFlags(param.flags() | QgsProcessingParameterDefinition.FlagAdvanced)
        self.addParameter(param)

        ## Hidden output folder
        out_folder = QgsProcessingParameterFolderDestination(
                OUT_CATCHMENTS_FOLDER,
                'Catchments Folder',               
                defaultValue=QgsProcessing.TEMPORARY_OUTPUT
        )
        out_folder.setFlags(out_folder.flags() | QgsProcessingParameterDefinition.FlagHidden)
        self.addParameter(out_folder)
            


    def processAlgorithm(self, parameters, context, feedback):
        """
        Here is where the processing itself takes place. This is called by QGIS.
        """

        if sys.stdout is None:
            sys.stdout = sys.__stdout__ or io.StringIO()
        if sys.stderr is None:
            sys.stderr = sys.__stderr__ or io.StringIO()
            
        self.wbt.set_verbose_mode(True)
        # self.wbt.set_callback(lambda p: feedback.setProgress(int(p)))


        ## Use a multi-step feedback, so that individual child algorithm progress reports are adjusted for the
        ## overall progress through the model
        self.total_steps = 100 # use for updating status bar
        self.step_counter = 0 # use for updating status bar
        feedback = QgsProcessingMultiStepFeedback(self.total_steps, feedback)
        
        results = {}

        ## Use time logs to track and test speed of processing
        start_time = pytime.time() # start timer
        recent_timestamp = [start_time] # store most recent lap time
        step_times = {} # store all times

        ## Use a python closure for time keeping
        def log_timer(step_name):
            """
            Reports the time between the last call and the current call
            """
            current_time = pytime.time()
            elapsed_time = current_time - recent_timestamp[0]
            step_times[step_name] = elapsed_time
            recent_timestamp[0] = current_time
            self.log(feedback, f"{step_name}, complete in {elapsed_time:.2f}s")


        ## Extract parameters from inputs
        defaults = DesignParams()
        area_factor = float(self.parameterAsDouble(parameters, PARAM_AREA_FACTOR, context))
        base_folder = self.parameterAsFile(parameters, PARAM_BASE_FOLDER, context)
        dem_layer: QgsRasterLayer = self.parameterAsRasterLayer(parameters, PARAM_DEM, context)
        road_layer: QgsVectorLayer = self.parameterAsVectorLayer(parameters, PARAM_ROAD, context)
        existing_ldd = self.parameterAsFile(parameters, PARAM_EXISTING_LDD, context)
        road_width = float(self.parameterAsDouble(parameters, PARAM_ROAD_WIDTH, context))
        snap_dist= float(self.parameterAsDouble(parameters, PARAM_SNAP_DIST, context))
        selected_runoff_method = self.parameterAsEnum(parameters, PARAM_RAIN_METHOD, context)  ## options=['Flavels RFFP2000 (Pilbara)' = 0,'Rational (basic, global)' = 1]
        pipe_diameters = defaults.pipe_diameters_m
        headwater_limit = self.parameterAsDouble(parameters, PARAM_HEADWATER_LIMIT, context)
        mannings_n = float(self.parameterAsDouble(parameters, PARAM_MANNINGS_N, context))

        ### Start the algorithm
        ##  Create Folders
        folders = initialise_folders(base_folder)
        log_timer("Folders created")

        if feedback.isCanceled():
            return {}
        self.update_progress(feedback)

        ##  Prepare the inputs
        (dem_layer, road_layer, dem_pcr_map_path) = prepare_inputs(context, feedback, folders, dem_layer, road_layer)
        log_timer("Initialised project")

        # delete ## parameters['catchments_output'] = folders['catchments'] #use this to open all the catchment files on completion

        if feedback.isCanceled():
            return {}
        self.update_progress(feedback)

        ## Create flow direction map
        ldd_output_path = create_ldd(context, feedback, folders, dem_pcr_map_path, existing_ldd)
        log_timer("Flow direction map creation")

        if feedback.isCanceled():
            return {}
        self.update_progress(feedback)

        ## Create strahler maps
        max_strahler_value = create_streamorder(context, feedback, folders, ldd_output_path)

        ## Select user defined strahler map for use
        # User provided stream order threshold
        threshold_order = int(self.parameterAsInt(parameters, PARAM_THRESHOLD_ORDER, context))

        if threshold_order > max_strahler_value:
            threshold_order = max_strahler_value
            feedback.pushInfo(f'Chosen threshold was above maximum, using maximum of {max_strahler_value} instead.')

        chosen_stream_map= os.path.join(folders['strahler'],'stream'+str(threshold_order)+'.map')
                
        road_intersections = find_road_intersections(context, feedback, folders, chosen_stream_map, road_layer)
        
        culvert_network_path = create_culvert_network(context, feedback, folders, dem_layer, road_intersections, road_width)
        culvert_network_empty = QgsVectorLayer(culvert_network_path, "1d_nwk", "ogr")
        log_timer("Culvert network generated")

        pour_points_path = extract_pour_points(context, feedback, folders, culvert_network_empty)

        dem_filled, flowdir, flowacc = whitebox_prepare(context, feedback, folders, dem_layer)
        processed_ids, catchment_filepaths, flowpath_filepaths = delineate_for_pour_points(context, feedback, folders, pour_points_path, dem_filled, flowdir, flowacc, snap_dist)
        log_timer("Catchments and streams generated")

        if feedback.isCanceled():
            return {}
        self.update_progress(feedback)

        flow_rates_by_id = compute_flow_rates(feedback, processed_ids, catchment_filepaths, flowpath_filepaths, selected_runoff_method, area_factor)
        log_timer("Flow rates calculated")

        if feedback.isCanceled():
            return {}
        self.update_progress(feedback)

        culvert_network_sized = size_culverts_HDS5(feedback, processed_ids, culvert_network_empty, flow_rates_by_id, pipe_diameters, headwater_limit, mannings_n)
        log_timer("Culverts designed")

        if feedback.isCanceled():
            return {}
        self.update_progress(feedback)

        if bool(self.parameterAsBool(parameters, PARAM_ADD_TO_PROJECT, context)):
            self.add_to_project(catchment_filepaths, flowpath_filepaths, culvert_network_sized)

        results[OUT_CATCHMENTS_FOLDER] = folders['catchments']
        return results


    def log(self, feedback, message):
        """
        Print messages to QGIS console and python console with timestamp
        """
        now = datetime.now()
        text = f"✏️ [{now.strftime('%H:%M:%S')}] {message}"
        feedback.pushInfo(text)
        print(text)

    def add_to_project(self, catchment_filepaths, flowpath_filepaths, culvert_network_sized):
        ## VISUALISATION IN CANVAS
        # ADD CATCHMENTS
        for filepath in catchment_filepaths:
            QgsProject.instance().addMapLayer(QgsVectorLayer(filepath, os.path.basename(filepath), "ogr"))
        # ADD FLOWPATHS
        for filepath in flowpath_filepaths:
            QgsProject.instance().addMapLayer(QgsVectorLayer(filepath, os.path.basename(filepath), "ogr"))
        # ADD CULVERTS
        QgsProject.instance().addMapLayer(culvert_network_sized)