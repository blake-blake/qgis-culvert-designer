PARAM_RAIN_METHOD = 'chosen_rainfall_analysis'
PARAM_BASE_FOLDER = 'base_folder'
PARAM_ROAD = 'road_alignment'
PARAM_DEM = 'dem'
PARAM_THRESHOLD_ORDER = 'threshold_order'
