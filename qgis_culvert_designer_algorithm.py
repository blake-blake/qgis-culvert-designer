# @dataclass(frozen = True)
# class DesignParams:
#     headwater_limit: float = 1.5
#     mannings_n: float = 0.024
#     snap_distance: float = 2.0 # consider making this relative to the cell size of the DEM
#     area_factor: float = 1.4
